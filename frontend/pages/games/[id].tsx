import { createApolloClient } from "@/graphql/client";
import { GameDocument, GameQuery } from "@/graphql/generated/graphql";
import { GetServerSideProps } from "next";
import { withUserTokenSSR } from "next-firebase-auth";
import Head from "next/head";
import Image from "next/image";

type PageProps = {
  game: GameQuery["game"];
  errorCode: string;
};

export function Game(props: PageProps) {
  if (props.errorCode) {
    return <div>{props.errorCode}</div>;
  }
  return (
    <div>
      <Head>
        <title>{props.game.id} | clip-games</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="mx-8 my-4">
        <div className="flex flex-column gap-5">
          <div className="relative h-96 w-64">
            <Image
              src={props.game.imageUrl || ""}
              alt={props.game.title}
              objectFit="contain"
              layout="fill"
            />
          </div>
          <div className="text-white flex">
            <p>{props.game.id}</p>
            <p>{props.game.title}</p>
            <p>{props.game.reviewsCount}</p>
            <p>{props.game.publishedAt}</p>
          </div>
        </div>
      </div>
    </div>
  );
}

export const getServerSideProps: GetServerSideProps = withUserTokenSSR()(
  async ({ user, query }) => {
    const { id } = query;
    if (typeof id !== "string") {
      return {
        notFound: true,
      };
    }
    const token = (await user?.getIdToken()) ?? undefined;
    const client = createApolloClient(token);

    const { data } = await client.query({
      query: GameDocument,
      variables: {
        gameId: id,
      },
    });
    const game = data.game;
    return { props: { game } };
  }
);
export default Game;
